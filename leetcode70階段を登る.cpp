class Solution {
public:
    int climbStairs(int n) {
        // 動的計画法（DP）の5つのステップを学ぶ
        
        // 1. DP配列とインデックスの意味を定義する
        // dp[i] は、i段目の階段に到達する方法の総数を表す
        
        // 2. 漸化式（再帰関係）を確立する
        // dp[i] = dp[i-1] + dp[i-2] 
        // （最後の1段か2段を登る方法の組み合わせ）
        
        // 3. DP配列の初期化
        // dp[1] = 1 （1段目に到達する方法は1通り）
        // dp[2] = 2 （2段目に到達する方法は2通り：1+1または2）
        
        // 4. 遍歷順序の決定
        // 前から後ろへ順番に計算する（i=3からnまで）
        
        // DP配列の宣言（サイズはn+1で、0番目は未使用）
        vector<int> dp(n + 1);
        
        // ベースケースの処理
        if (n <= 1) return n; // n=0の場合は0、n=1の場合は1を返す
        
        // 初期条件の設定
        dp[1] = 1;
        dp[2] = 2;
        
        // ボトムアップ方式でDPテーブルを埋める
        for (int i = 3; i <= n; i++) {
            dp[i] = dp[i - 1] + dp[i - 2]; // 漸化式に従って計算
        }
        
        // n段目の結果を返す
        return dp[n];
    }
};